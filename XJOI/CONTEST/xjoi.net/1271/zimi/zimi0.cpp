using namespace std;

inline int read(){
	int ret=0,f=1;char ch=getchar();
	while (ch<'0'||ch>'9') {if (ch=='-') f=-1;ch=getchar();}
	while (ch>='0'&&ch<='9') ret=ret*10+ch-'0',ch=getchar();
	return ret*f;
}

int n;

int qsm(int a,int b){
	int ret=1,w=a;
	while (b){
		if (b&1) ret=ret*w;
		w=w*w; b>>=1;
	}
	return ret;
}

bool check(int x){
	int now=0,rst=x;
	while (x){
		now+=qsm(x%10,n);
		x/=10;
	}
	return now==rst;
}

signed main(){
	n=read();
	int l=qsm(10,n-1),r=l*10;
	for (int i=l;i<r;i++) if (check(i)) printf("%lld ",i);
	printf("\n");
	return 0;
}